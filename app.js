function setLanguage(lang) {
  if (!translations[lang]) return;
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Update page titles & metadata
  document.title = translations[lang]["meta-title"];
  const descMeta = document.querySelector('meta[name="description"]');
  if (descMeta) descMeta.setAttribute('content', translations[lang]["meta-desc"]);
  
  const ogTitleMeta = document.querySelector('meta[property="og:title"]');
  if (ogTitleMeta) ogTitleMeta.setAttribute('content', translations[lang]["meta-title"]);
  
  const ogDescMeta = document.querySelector('meta[property="og:description"]');
  if (ogDescMeta) ogDescMeta.setAttribute('content', translations[lang]["meta-desc"]);
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  
  // Update active state in switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });
  
  // Persist language selection
  localStorage.setItem('preferred-lang', lang);
}

// Initialization routine
document.addEventListener('DOMContentLoaded', () => {
  // 1. Check URL parameters
  const urlParams = new URLSearchParams(window.location.search);
  const urlLang = urlParams.get('lang');
  
  // 2. Check localStorage
  const storedLang = localStorage.getItem('preferred-lang');
  
  // 3. Check browser language
  const browserLang = navigator.language.startsWith('id') ? 'id' : 'en';
  
  // Determine final language choice
  let defaultLang = 'en'; // default fallback
  if (urlLang && translations[urlLang]) {
    defaultLang = urlLang;
  } else if (storedLang && translations[storedLang]) {
    defaultLang = storedLang;
  } else {
    defaultLang = browserLang;
  }
  
  setLanguage(defaultLang);
  
  // Bind click event handlers
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang');
      setLanguage(lang);
    });
  });
  
  // Initialize Lucide Icons
  lucide.createIcons();
});
